# Tool	Command                Example	Description
# ping	ping google.com	        Checks if a host is reachable and measures latency
# tracert	tracert google.com	 Shows the path packets take to reach a host
# whois	whois google.com	   Retrieves domain registration information
# nslookup	nslookup google.com	      Displays DNS records (A, MX, NS, etc.)
# ipconfig	ipconfig /all	     Shows network configuration on Windows
# hostname	hostname	            Displays the local machine's name
# arp	arp -a	                 Shows IP-to-MAC address mappings
# netstat	netstat -an	         Displays active connections and listening ports
# route	route print	            Shows the system's routing table
# systeminfo	systeminfo	     Displays detailed system configuration
